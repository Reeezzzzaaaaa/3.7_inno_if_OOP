package Branches.Task_1;

public class name_if {

    public static void main(String[] args) {

        int balance;

        for (int i = 0; i <= 11; i++) {
            balance = i;
            if (balance == 10) {
                System.out.println("Десятка");
            } else {
                System.out.println(balance);
            }
        }

    }
}
